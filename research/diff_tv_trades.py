TOL = 15 * 60 * 1000
matched, tv_only = [], []
used = set()
for side, tm, ep, xtm, xp, xc, pnl in TV:
    cands = [t for t in loc_by_key.get(side, [])
             if abs(t["entry_ts"] - tm) <= TOL and id(t) not in used]
    if cands:
        t = min(cands, key=lambda t: abs(t["entry_ts"] - tm))
        used.add(id(t))
        matched.append((side, tm, ep, xc, pnl, t))
    else:
        tv_only.append((side, tm, ep, xc, pnl))

local_only = [t for side in loc_by_key for t in loc_by_key[side] if id(t) not in used]

